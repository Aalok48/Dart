void main() {
  var grade = 'D';
  switch (grade) {
    case 'A':
      print("Excellant Grade");
      break;
    case 'B':
      print('Good Grade');
      break;
    case 'C':
      print('Try to get better');
      break;
    case 'D':
      print('Ahh, you piece of SHIT');
      break;
    default:
      print(("Grade not found..."));
  }
}


// Only integer and string variables can be used with Switch statement